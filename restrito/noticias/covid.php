            <h1>Prevenção à covid-19</h1>
               <div class="single_page_content"> <img class="img-center" src="https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcTIQMK22mDd7AqxZR4T_ZDth8GNj-Auj5yCnmDG1MRj3y1QZkkUlRkAB79_sw5JzwkY5YQ&usqp=CAU" alt="">
              <p>Durante a pandemia de covid-19, as medidas de prevenção e de estímulo à saúde e segurança no trabalho ganharam ainda mais importância. A publicação da OIT “Garantir a segurança e saúde no trabalho durante a pandemia” afirma que é “necessário  um acompanhamento contínuo das condições de SST e uma avaliação adequada dos riscos para garantir que as medidas de controle relacionadas com o risco de contágio sejam adaptadas aos processos, condições de trabalho e características específicas da mão de obra durante o período crítico de contágio e posteriormente”.</p>
              <blockquote> O documento traz uma série de riscos profissionais e medidas de proteção de saúde e segurança do trabalho durante a pandemia. Para controlar o risco de contágio, por exemplo, é indicado fazer uma avaliação da probabilidade de contágio e gravidade dos dados para a saúde - com avaliações sobre questões de distanciamento físico, higiene, limpeza do local, uso de EPI, etc. Também há medidas específicas para profissionais da saúde (controle ambiental para reduzir a propagação de agentes patógenos), trabalhadores de laboratório, profissionais se serviços dos cuidados da morte, de transporte de emergência, etc. </blockquote>
              <h2>Em resumo, são quatro pilares fundamentais na resposta à crise do coronavírus:</h2>
              
              <ul>
                <li>1. Estimular a economia e o emprego, através de políticas orçamentais ativas, políticas monetárias mais favoráveis de empréstimos e de apoio financeiro a setores específicos, incluindo o setor da saúde. </li>
                
                <li>2. Apoiar as empresas, o emprego e os rendimentos, através do alargamento da proteção social a todos, da aplicação de medidas de retenção de emprego, da concessão de benefícios financeiros/fiscais e outros destinados às empresas. </li>
                <li>3. Proteger os trabalhadores e as trabalhadoras nos locais de trabalho, reforçando as medidas de SST, adotando modalidades de trabalho flexíveis (por exemplo, teletrabalho), prevenindo a discriminação e a exclusão, proporcionando acesso à saúde para todos e expandindo o acesso a licenças remuneradas. </li>
                <li>4. Utilizar o diálogo social para soluções, reforçando a capacidade e a resiliência das organizações de empregadores e de trabalhadores, reforçando a capacidade dos governos, do diálogo social, das instituições e dos processos de negociação coletiva e de relações laborais.</li>
                
              </ul>


