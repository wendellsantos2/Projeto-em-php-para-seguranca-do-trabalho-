            <h1>9 informações essenciais sobre Segurança do Trabalho.</h1>
                  <div class="single_page_content"> <img class="img-center" src="https://blog.mastt.com.br/wp-content/uploads/2017/07/9-informacoes-essenciais-sobre-seguranca-do-trabalho-750x410.jpeg" alt="">
              <blockquote>A segurança do trabalho é um conjunto de atividades focadas na proteção à integridade e à capacidade de trabalho dos colaboradores das empresas. Seu principal objetivo é melhorar as condições do ambiente laboral e minimizar as ocorrências de acidentes e doenças ocupacionais.

                Reunimos neste artigo informações essenciais sobre o assunto que vão levá-lo a entender, valorizar e, principalmente, investir em segurança do trabalho na sua empresa. Confira! </blockquote>
              <ul>
                <li>1. Quais são os fundamentos da segurança do trabalho?</li>
                <li>2. Quando uma empresa é obrigada a ter um profissional de segurança do trabalho?</li>
                <li>3. Quais são os profissionais de segurança do trabalho?</li>
                <li>4. Qual o papel de cada profissional do SESMT?</li>
                <li>5. Como prevenir doenças ocupacionais?</li>
                <li>6. Como prevenir acidentes do trabalho?</li>
                <li>7. O que é EPC?</li>
                <li>8. O que é EPI?</li>
                <li>9. O que minha empresa ganha com a segurança do trabalho?</li>
              </ul>
              <h1>1. Quais são os fundamentos da segurança do trabalho?</h1>
              <p1>Existem diversas normas e leis em que se fundamenta a segurança do trabalho. A maioria destas são determinadas pelo Ministério do Trabalho e Emprego.

                Dentre elas temos Normas Regulamentadoras (mais conhecidas como NRs), decretos, portarias, leis complementares e convenções internacionais da OIT (Organização Internacional do Trabalho), das quais o Brasil é signatário.</p1>
              <h1>2. Quando uma empresa é obrigada a ter um profissional de segurança do trabalho?</h1>
              <p1>A obrigatoriedade de ter um profissional de segurança do trabalho é definida em função do cruzamento de três informações: a atividade econômica da empresa, o grau de risco associado à atividade econômica e o número de empregados que a empresa tem.

                Basta consultar a norma regulamentadora do Ministério do Trabalho e Emprego, NR-4 – Serviços Especializados em Engenharia de Segurança e em Medicina do Trabalho, para saber quantos e quais serão os profissionais exigidos para a sua organização ou mesmo se a sua empresa está desobrigada de tê-los em seu quadro de colaboradores.</p1>
              <h1>3. Quais são os profissionais de segurança do trabalho?</h1>
              <p1>O Serviço Especializado em Engenharia de Segurança e em Medicina do Trabalho (SESMT) pode ser composto pelos seguintes profissionais: Engenheiro de Segurança do Trabalho, Técnico de Segurança do Trabalho, Médico do Trabalho e Enfermeiro/Auxiliar de Enfermagem do Trabalho.</p1>
              <h1>4. Qual o papel de cada profissional do SESMT?</h1>
              <p1>O Engenheiro e o Técnico de Segurança do Trabalho são responsáveis por estabelecer programas e medidas de prevenção de riscos ambientais e acidentes, ministrar treinamentos de uso de equipamentos de proteção individual e combate a incêndio, realizar inspeções de segurança, investigar acidentes, emitir laudos técnicos e fazer palestras de conscientização.

                O Médico do Trabalho e o Enfermeiro/Auxiliar de Enfermagem do Trabalho têm suas atividades voltadas para a prevenção de doenças ocupacionais, sendo responsáveis por consultas e exames médicos, atendimento ambulatorial, primeiros socorros, ministração de vacinas e palestras e treinamentos de saúde e higiene ocupacional.</p1>
              <h1>5. Como prevenir doenças ocupacionais?</h1>
              <p1>Quando algum fator relacionado ao trabalho desencadeia um problema de saúde no trabalhador, está caracterizada uma doença ocupacional. Ela pode ser evitada com algumas medidas, como:
                 <br></br>
                <li>1. móveis e equipamentos ergonomicamente adequados ao trabalhador;</li>
                <li>2. boas práticas de higiene e saúde ao trabalhador por meio de palestras e treinamentos;</li>
                <li>3.fazer campanhas de conscientização;</li>
                <li>4. exames médicos de saúde periódicos dos trabalhadores;</li>
                <li>5.cumprir as normas regulamentadoras e legislações específicas;</li>
                <li>6.reduzir a exposição do trabalhador às situações que gerem riscos à sua saúde.</li></p1>
              <h1>6. Como prevenir acidentes do trabalho?</h1>
              <p1>Se um trabalhador se acidenta em decorrência do exercício do trabalho a serviço de uma empresa, está caracterizado um acidente do trabalho. Ele pode ter consequências como lesão corporal, perda ou redução temporária ou permanente da capacidade para o trabalho ou até resultar na morte do colaborador.
                <br></br>

                Para prevenir acidentes do trabalho a empresa precisa determinar medidas de controle dos riscos ambientais, que devem obedecer a uma hierarquia de prioridade:
                <br></br>
               <li>1. eliminação – verificar se existe algum meio de eliminar o risco por meio de ações, como a alteração no layout do local de trabalho ou no próprio processo de trabalho – mudança de metodologia ou tecnologia;</li>
               <li>2.substituição – substituir materiais, produtos, máquinas e equipamentos perigosos por outros mais seguros;</li>
               <li>3.controles de engenharia – instalar barreiras, sistemas de ventilação, dispositivos de segurança e equipamentos de proteção coletiva (EPCs), segregar (isolar) fontes de perigo, entre outras medidas possíveis para minimizar o risco e/ou a exposição do trabalhador;</li>
               <li>4.sinalização, alertas e controles administrativos – instalar placas, sinais sonoros e luminosos, delimitar áreas com cores específicas, elaborar cartilhas e checklists de inspeção de segurança, fazer etiquetagem e ministrar treinamentos de segurança e operacionais são exemplos deste tipo de controle;</li>
               <li>5.equipamentos de proteção individual (EPIs) – fornecer os equipamentos de proteção individual que minimizem os impactos de um acidente de trabalho, ensinar o modo correto de utilizá-los e inspecionar o uso dos colaboradores. Esta medida somente deve ser tomada após esgotadas as possibilidades das medidas de controle anteriores.</li>
              </p1>
              <h1>7. O que é EPC?</h1>
              <p1>Os equipamentos de proteção coletiva (EPCs) são voltados para a proteção de todos os trabalhadores que estiverem em sua proximidade. Existem diversos tipos de EPCs e dentre eles podemos citar:
                 <br></br>
               <li>1. guarda-corpo;</li>
               <li>2.corrimão;</li>
               <li>3.piso antiderrapante;</li>
               <li>4.fitas sinalizadoras (fita zebrada);</li>
               <li>5.cavaletes, cones e placas de sinalização;</li>
               <li>6.tela de proteção perimetral (evita projeção de materiais);</li>
               <li>7.plataforma de proteção (evita queda de entulhos).</li>
               <li>8.exaustores.</li>
              </p1>
              <h1>8. O que é EPI?</h1>
              <p1>Os equipamentos de proteção individual (EPIs) são destinados a proteger cada trabalhador particularmente. Dentre os diversos tipos existentes podemos exemplificar:
                  <br></br>
               <li>1. óculos de segurança;</li>
               <li>2.botinas;</li>
               <li>3.luvas;</li>
               <li>4.capacetes;
               <li>5.abafadores de ruídos e protetores auriculares;</li>
               <li>6.viseiras e máscaras faciais;</li>
               <li>7.uniformes antichamas;</li>
               <li>8.coletes refletivos.</li>
              </p1>
              <h1>9. O que minha empresa ganha com a segurança do trabalho?</h1>
              <p1>A segurança do trabalho é primordial para a qualidade de vida dos trabalhadores – seu bem-estar físico, emocional e espiritual. Ela assegura condições para que os colaboradores desenvolvam suas tarefas diárias com tranquilidade e com conhecimento dos riscos ambientais a que estão sujeitos e das medidas de segurança que devem tomar para a proteção própria e coletiva.

                Vale lembrar que empregados satisfeitos produzem mais e melhor e faltam menos ao trabalho, pois não se envolvem em acidentes e preservam sua saúde.
                
                Os investimentos em segurança do trabalho também são importantíssimos para a preservação da saúde financeira das empresas, pois asseguram o cumprimento dos requisitos legais, evitando a ocorrência de multas e indenizações trabalhistas por acidentes e doenças ocupacionais.
                
                Viu como estas informações essenciais podem ajudar a sua empresa a ter práticas que contribuam para que todos trabalhem em um ambiente saudável e com os riscos controlados?</p1>
              
             
