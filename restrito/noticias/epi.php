            <h1>EPIs salvam vidas</h1>
                         <div class="single_page_content"> <img class="img-center" src="https://www.tst.jus.br/documents/10157/70971/EPIs+27-07.png/a20ff383-9fd8-a758-87b1-42c9d6f8b773?t=1627408571414imagePreview=1" alt="">
              <p>Conhecidos os perigos a que algumas atividades econômicas estão sujeitas, como evitar que tragédias aconteçam? Por meio de medidas de prevenção a acidentes de trabalho, sendo o Equipamento de Proteção Individual (EPI) uma dos meios mais básicos e conhecidos instrumentos para tal.

                De acordo com a Norma Regulamentadora nº 6 do Ministério do Trabalho, considera-se Equipamento de Proteção Individual (EPI) “todo dispositivo ou produto, de uso individual utilizado pelo trabalhador, destinado à proteção de riscos suscetíveis de ameaçar a segurança e a saúde no trabalho”. O normativo traz ainda a obrigação da empresa de fornecer aos empregados, gratuitamente, EPI adequado ao risco, em perfeito estado de conservação e funcionamento, sempre que as medidas de ordem geral não ofereçam completa proteção contra os riscos de acidentes do trabalho ou de doenças profissionais e do trabalho; enquanto as medidas de proteção coletiva estiverem sendo implantadas; e para atender a situações de emergência.</p>
              <blockquote> Na legislação federal, a Consolidação das Leis do Trabalho (CLT) dispõe, no artigo 166, que “a empresa é obrigada a fornecer aos empregados, gratuitamente, equipamento de proteção individual adequado ao risco e em perfeito estado de conservação e funcionamento, sempre que as medidas de ordem geral não ofereçam completa proteção contra os riscos de acidentes e danos à saúde dos empregados”. Assim, o EPI não apenas deve ser fornecido de forma gratuita como também deve ser adequado à atividade desempenhada. </blockquote>
              <h2>Tipos de EPIs</h2>
              <p>Os EPIs são classificados a partir da parte do corpo a ser protegida e da atividade desempenhada, divididos pela NR 6 em nove categorias:</p>
              <ul>
                <li>1. proteção da cabeça: capacete, capuz ou balaclava;</li>
                <li>2. proteção dos olhos e face: óculos, protetor facial, máscara de solda;</li>
                <li>3. proteção auditiva: protetor auditivo circum-auricular, de inserção, ou semi-auricular;</li>
                <li>4. proteção respiratória: respirador purificador de ar não motorizado ou motorizado; de adução de ar, ou de fuga;</li>
                <li>5. proteção do tronco: vestimentas para proteção, colete à prova de balas;</li>
                <li>6. proteção dos membros superiores: luvas, creme protetor, manga, braçadeira, dedeira;</li>
                <li>7. proteção dos membros inferiores: calçados para proteção, meia, perneira, calça; </li>
                <li>8. proteção do corpo inteiro: macacão; vestimentas de corpo inteiro;</li>
                <li>9. proteção contra quedas com diferença de nível: cinturão de segurança com dispositivo trava-queda, cinturão de segurança com talabarte.</li>
              </ul>




