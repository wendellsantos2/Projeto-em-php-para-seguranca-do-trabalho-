          <h1>1. Dados pessoais</h1>
          <li>Os dados pessoais, em um currículo moderno, devem ser concisos e listar apenas informações fundamentais. Isso inclui nome completo, idade, número de telefone e e-mail. Lembre-se que o e-mail deve conter, preferencialmente, apenas nome e sobrenome. Nada de apelidos ou diminutivos.

            No caso dos telefones, você também pode incluir números para recado. É só colocar a indicação “recados com” e o nome da pessoa. Se desejar, pode incluir, ainda, o endereço. Porém, não precisa colocar os dados completos, apenas bairro, cidade e estado.
            
            É importante ressaltar que você não deve colocar no currículo seus números de RG e CPF. Já as fotos, só são necessárias para empregos específicos, como de modelo ou atriz/ator.</li>
          <h1>2. Habilidades e Competências</h1>
       
      </div>
       <li>
        Seguindo com a elaboração do currículo, chegamos na área destinada às suas competências e habilidades. Esse é o momento de apresentar os seus diferenciais para a vaga que deseja.

        Aqui você pode listar as habilidade técnicas que possui, incluindo:
        
        domínio de programas, como Photoshop e AutoCad;
        conhecimentos específicos, por exemplo de copywriter, Ux Design, Direito Ambiental, Nutrição esportiva, etc.
        Também é importante apresentar suas competências de forma geral. Assim, você pode dizer que tem facilidade com liderança, trabalho sob pressão, oratória, inteligência emocional, entre outros temas adequados ao seu perfil.</li>
        
        
        <h1>3. Experiência Profissional</h1>
        <li>Para você que está aprendendo como montar um currículo, é importante ter em mente que a experiência profissional é uma das seções que merece maior atenção. É aqui que os recrutadores vão analisar toda a sua vivência no mercado de trabalho, verificando se o seu perfil combina com o da vaga em aberto.

          A formatação dessa etapa não é muito diferente daquela usada na formação acadêmica. Ou seja, você precisa listar os locais em que trabalhou em ordem inversa: do mais recente ao mais antigo.
          
          Caso tenha muitas experiências, coloque apenas as que são mais relevantes para o cargo desejado. Em contrapartida, se estiver em busca do primeiro emprego, dê ênfase nas atividades de estágio, monitoria e de trabalho voluntário que já tenha feito.
          
          Apresente também as conquistas alcançadas em cada emprego. Isso agrega muito mais valor do que apenas listar suas responsabilidades.</li>
